#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	size_t		total_size;
	char		*ptr;

	total_size = nmemb * size;
	if (size != 0 && total_size / size != nmemb)
		return (NULL);
	ptr = malloc(total_size);
	if (!ptr)
		return (NULL);
	ft_memset(ptr, 0, total_size);
	return (ptr);
}
